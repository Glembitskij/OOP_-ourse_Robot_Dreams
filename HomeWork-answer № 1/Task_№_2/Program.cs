// Розв'язок
// Створення екземпляра класу «Кошеня» та присвоєння значень полям через властивості доступу
Kitten kitten = new Kitten
{
    Name = "Fluffy",
    Age = 2,
    FurColor = "White",
    EyeColor = "Blue"
};

// Виклик методу, який виводить «Мяу» 3 рази
kitten.Meow(3);

internal class Kitten
{
    // Поля класу
    private string? name;
    private int age;
    private string? furColor;
    private string? eyeColor;

    // Властивості доступу до поля name
    public string? Name
    {
        get { return name; }
        set { name = value; }
    }

    // Властивості доступу до поля age
    public int Age
    {
        get { return age; }
        set { age = value; }
    }

    // Властивості доступу до поля furColor
    public string? FurColor
    {
        get { return furColor; }
        set { furColor = value; }
    }

    // Властивості доступу до поля eyeColor
    public string? EyeColor
    {
        get { return eyeColor; }
        set { eyeColor = value; }
    }

    // Метод, який виводить «Мяу» певну кількість разів
    public void Meow(int times)
    {
        for (int i = 0; i < times; i++)
        {
            Console.WriteLine("Мяу");
        }
    }
}